public class MOD_2B {
    public static void main(String[] args) { 
        int a = 6, b = 4;

        System.out.println("Nilai a + b = " + (a + b));
        System.out.println("Nilai a - b = " + (a - b));
        System.out.println("Nilai a * b = " + (a * b));
        System.out.println("Nilai a / b = " + (a / b));
        System.out.println("Nilai a % b = " + (a % b));
        System.out.println("Nilai ++a = " + (++a));
        System.out.println("Nilai b++ = " + (b++));
        System.out.println("Nilai b-- = " + (b--));
        System.out.println("Nilai --a = " + (--a));
    }
}
